package com.sample.fish;

public class Shark extends Fish {

	public void sing() {
		System.out.println("I am saying Cluck, cluck");
	}

	public void fly() {
		System.out.println("I am not flying");
	}

	public void color() {
		System.out.println("I am gray color");
	}

	public void size() {
		System.out.println("I am large size");
	}

	public void eat() {
		System.out.println("I am eating other fish");
	}
}